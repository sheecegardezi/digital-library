package datastructures;

public class Word {
	String token;
	String pos;
	
	public Word(String token,String pos){
		token=this.token;
		pos=this.pos;	
	}
	
	
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getPos() {
		return pos;
	}
	public void setPos(String pos) {
		this.pos = pos;
	}

}
